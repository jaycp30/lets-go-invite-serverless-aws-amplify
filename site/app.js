async function loadInvitePage() {
  const inviteId = new URLSearchParams(window.location.search).get('id');
  const yesBtn = document.querySelector('.btn-yes');
  const noBtn = document.getElementById('noButton');
  if (yesBtn) yesBtn.style.visibility = 'hidden';
  if (noBtn) noBtn.style.display = 'none';

  if (!inviteId || !API_URL) {
    showUnavailableInvite('This invitation is unavailable.');
    return;
  }

  try {
    const data = await fetchInvite(inviteId);
    document.getElementById('inviteName').textContent     = `Hey ${data.recipientName || 'You'}!`;
    document.getElementById('inviteMessage').textContent  = data.message;
    document.getElementById('inviteActivity').textContent = data.activity;
    document.getElementById('inviteDate').textContent     = data.date ? formatDate(data.date) : '';

    injectCSS(data.buttonAnimCSS || FALLBACK_BUTTON_ANIM);
    if (data.mascotIntro) showSpeechBubble(data.mascotIntro, 4000);

    initializeNoButton();
    if (yesBtn) {
      yesBtn.href = `yes.html?id=${encodeURIComponent(inviteId)}`;
      yesBtn.style.visibility = '';
    }
  } catch (err) {
    console.error(err);
    showUnavailableInvite(err.message || 'This invitation is unavailable.');
  }
}

function initializeNoButton() {
  // Initialize No button: translate CSS bottom/right to top/left immediately
  // so Firefox never sees conflicting offset properties, then bind events via JS.
  const noBtn = document.getElementById('noButton');
  if (noBtn) {
    noBtn.style.display = '';
    const vh = (window.visualViewport?.height) || window.innerHeight;
    noBtn.style.bottom = 'auto';
    noBtn.style.right  = 'auto';
    noBtn.style.top    = `${vh - noBtn.offsetHeight - 24}px`;
    noBtn.style.left   = `${window.innerWidth - noBtn.offsetWidth - 24}px`;
    noBtn.addEventListener('pointerenter', dodgeNo);
    noBtn.addEventListener('pointerover', dodgeNo);
    noBtn.addEventListener('mouseenter', dodgeNo);
    noBtn.addEventListener('mouseover', dodgeNo);
    noBtn.addEventListener('pointerdown', dodgeNo);
    noBtn.addEventListener('click', dodgeNo);
    noBtn.addEventListener('touchstart', (e) => { e.preventDefault(); dodgeNo(); }, { passive: false });
  }
}

function showUnavailableInvite(message) {
  document.getElementById('inviteMessage').textContent = message;
  document.getElementById('inviteActivity').textContent = '';
  document.getElementById('inviteDate').textContent = '';
  const yesBtn = document.querySelector('.btn-yes');
  if (yesBtn) yesBtn.style.display = 'none';
  const noBtn = document.getElementById('noButton');
  if (noBtn) noBtn.style.display = 'none';
}

// ── No button — dodge + CSS animation + mascot reaction ──────────────────────

function dodgeNo() {
  const btn = document.getElementById('noButton');
  if (!btn) return;

  // Debounce: skip if called again within 400ms (prevents mobile double-fire from mouseover+click)
  const now = Date.now();
  if (now - lastDodgeAt < 400) return;
  lastDodgeAt = now;

  // Bounds: container width horizontally, visible viewport vertically
  const container = document.querySelector('.container');
  const pad = 16;
  const rect = container ? container.getBoundingClientRect() : null;
  const vw = window.innerWidth;
  const vh = (window.visualViewport?.height) || window.innerHeight;
  const x1 = rect ? rect.left  + pad : vw * 0.08;
  const x2 = rect ? rect.right - btn.offsetWidth  - pad : vw * 0.92 - btn.offsetWidth;
  const y1 = pad;
  const y2 = vh - btn.offsetHeight - pad;

  // Limit travel: move at most 35% of the available range from current position
  const curLeft = parseFloat(btn.style.left);
  const curTop  = parseFloat(btn.style.top);
  const hasPos  = !isNaN(curLeft) && !isNaN(curTop);
  const maxStep = Math.min(x2 - x1, y2 - y1) * 0.35;

  let newLeft, newTop;
  if (hasPos) {
    newLeft = curLeft + (Math.random() - 0.5) * 2 * maxStep;
    newTop  = curTop  + (Math.random() - 0.5) * 2 * maxStep;
  } else {
    newLeft = x1 + Math.random() * Math.max(0, x2 - x1);
    newTop  = y1 + Math.random() * Math.max(0, y2 - y1);
  }

  // Clear CSS bottom/right so top/left don't conflict with them
  btn.style.bottom = 'auto';
  btn.style.right  = 'auto';
  btn.style.left = `${Math.max(x1, Math.min(x2, newLeft))}px`;
  btn.style.top  = `${Math.max(y1, Math.min(y2, newTop))}px`;

  // Replay the escape animation
  btn.style.animation = 'none';
  btn.offsetHeight; // force reflow so animation restarts
  btn.style.animation = 'noEscape 0.45s ease';

  // Show instant fallback reaction, then replace with AI line
  const fallback = FALLBACK_REACTIONS[Math.floor(Math.random() * FALLBACK_REACTIONS.length)];
  showSpeechBubble(fallback, null);

  if (!isFetching) fetchReaction();
}

async function fetchReaction() {
  if (!API_URL) { scheduleBubbleHide(3000); return; }
  isFetching = true;
  try {
    const res = await fetch(`${API_URL}/generate`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ type: 'reaction' }),
    });
    if (!res.ok) throw new Error(`HTTP ${res.status}`);
    const data = await res.json();
    showSpeechBubble(data.reaction, 3000);
  } catch {
    scheduleBubbleHide(3000);
  } finally {
    isFetching = false;
  }
}

function showSpeechBubble(text, autohideMs) {
  const bubble = document.getElementById('speechBubble');
  if (!bubble) return;
  bubble.textContent = text;
  bubble.classList.add('visible');
  clearTimeout(bubbleTimer);
  if (autohideMs) scheduleBubbleHide(autohideMs);
}

function scheduleBubbleHide(ms) {
  clearTimeout(bubbleTimer);
  bubbleTimer = setTimeout(() => {
    const bubble = document.getElementById('speechBubble');
    if (bubble) bubble.classList.remove('visible');
  }, ms);
}

